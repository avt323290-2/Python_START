# Задача 32: Определить индексы элементов массива (списка), значения которых принадлежат заданному диапазону (т.е. не меньше заданного минимума и не больше заданного максимума)

# Получаем входные данные от пользователя
nums = input("Введите числа через пробел: ").split()
min_val = float(input("Минимальное значение: "))
max_val = float(input("Максимальное значение: "))

# Создаем список индексов, удовлетворяющих условию
indices = [i for i in range(len(nums)) if min_val <= float(nums[i]) <= max_val]

# Выводим результат
print(f"Индексы элементов, значения которых находятся в диапазоне [{min_val}, {max_val}]: {indices}")
